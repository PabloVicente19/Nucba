// Hoisting
/*
En Javascript el concepto de hoisting hace referencia al contexto de ejecucion. Es decir, JavaScript internamente eleva al comienzo del programa(internamente) a las variables y funciones.
*/

// Scope
/*
El Scope hace referencia al contexto de ejecucion actual, es decir, todas las expreciones que son "visibles" en el scope actual. Una funcion que contiene una variable creada dentro de ella, el scope(ambito) de esa variable, solo se limita al de la funcion, es decir, no se puede llamar a la variable estando fuera de la funcion.
Podemos crear variables con el scope global, 
*/

